package model;

public class Badanie {
	
	
	String nazwa;
	String opis;
	int koszt;
	
	
	public String getNazwa() {
		return nazwa;
	}
	public void setNazwa(String nazwa) {
		this.nazwa = nazwa;
	}
	public String getOpis() {
		return opis;
	}
	public void setOpis(String opis) {
		this.opis = opis;
	}
	public int getKoszt() {
		return koszt;
	}
	public void setKoszt(int koszt) {
		this.koszt = koszt;
	}

	
}
